  <h1 class="c4"><a name="h.6k8p0kyxzyej" id="h.6k8p0kyxzyej"></a><span>#SandyAid
  Followup</span></h1>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c3">What:</span><span>&nbsp;This project would try to
  systematize tracking and follow up to aid requests using the #SandyAid
  hashtag.</span></p>

  <p class="c4 c9"></p>

  <p class="c4"><span>People are currently using the hashtag</span> <span>#SandyAid to
  request non-emergency community assistance.</span> <span class="c7">We take these
  tweets, curate them (finding the actual requests among the RTs and expressions of
  support),and add them to a DB. Then, we try to be sure there&rsquo;s
  followup.</span></p>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c3 c7">How:</span><span class="c7">&nbsp;</span><span>At the
  most basic: cut and paste the concrete requests from the #SandyAid feed into a
  spreadsheet. Open it to people taking them on. Constantly sort by urgency, mark when
  complete.</span></p>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c7">Subtasks include:</span></p>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c7">1. monitor #SandyAid tweets (via RSS, link to describe
  how to pull RSS from tag)</span></p>

  <p class="c4"><span class="c7">2. filter them to find the real requests (currently
  manually, with volunteers, in freenode #SandyAid)</span></p>

  <p class="c4"><span class="c7">3. get them into a tracking system (cut and paste? into
  a web UI, from the irc channel?)</span></p>

  <p class="c4"><span class="c7">4. get people to take them on (via the web UI?
  &lsquo;I&rsquo;ll work on this one!)</span></p>

  <p class="c4"><span class="c7">5. mark them when complete (via the web UI)</span></p>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c11">Potential categories (in potential descending order of
  urgency):</span></p>

  <p class="c4"><span class="c11">Note: I don&rsquo;t think we should encourage people to
  use in this way. Keep repeating to folks that #SandyAid is for non-urgent/non-emergency
  requests. Route these kind of urgent requests to other responders w/on the ground
  capacity. - @schock (We did definitely see tweets on these subjects though so
  we&rsquo;ll have to decide how we want to handle them. -- RW) yeah, for
  sure.</span></p>

  <p class="c4"><span class="c11">1. Personal danger</span></p>

  <p class="c4"><span class="c11">2. Missing persons</span></p>

  <p class="c4"><span class="c11">3. Rescue &larr; (This would classify as urgent
  then.)</span></p>

  <p class="c4"><span class="c11">4. Food/water</span></p>

  <p class="c4"><span class="c11">5. Shelter</span></p>

  <p class="c4"><span class="c11">6. Cleanup</span></p>

  <p class="c4"><span class="c11">7. Donations</span></p>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c11">&nbsp;</span></p>

  <p class="c4"><span class="c11">Currently all tweets with the tag are being filtered by
  a bot through the freenode IRC chat #sandyaid. Stop in and help filter relevant tweets
  by typing &ldquo;sandybot: {tweetid}&rdquo; to have the bot retweet it to our Twitter
  account</span> <span class="c0 c11"><a class="c8" href=
  "http://www.twitter.com/SandyAid">http://www.twitter.com/SandyAid</a></span></p>

  <p class="c4 c9"></p>

  <p class="c4"><span>Dev status (</span><span class="c0"><a class="c8" href=
  "https://github.com/b1naryth1ef/sandyslist">GH Repository</a></span><span>):</span>
  <span class="c36">Adding management of submissions (possibly login system, not sure
  yet). Wave is working on design (Maybe?)</span></p>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c11">B1naryTh1ef is taking lead on development (Python,
  MongoDB), wavesounds is helping, and doing the design. Discussion also of integrating
  Twilio text messaging into system.</span></p>

  <p class="c4 c9"></p>

  <p class="c4 c9"></p>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c3 c11">CURRENTLY IN PROGRESS:</span></p>

  <p class="c4"><span class="c11">We would like to update this system so that these
  monitored tweets are filtered for actual requests for help which are then stored in a
  database or spreadsheet according to time, location, category, and contact info. This
  data is then accessible by anyone looking to help, who can search this database to find
  local need and can sign up to assist with a particular request and provide their own
  contact info.</span></p>

  <p class="c4 c9"></p>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c3 c11">User story 1:</span> <span class="c11">I would like
  to request assistance with a hurricane-related problem; I will tweet the hashtag
  #SandyAid with my information.</span></p>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c3 c11">User story 2:</span><span class="c11">&nbsp;I have
  {food, an extra room, first-aid knowledge, money, etc.} and would like to share it with
  hurricane victims. I will search the database to find issues in my area.</span></p>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c3 c11">User story 3:</span><span class=
  "c11">&nbsp;I&rsquo;m far away but have internet and want to help. I&rsquo;ll review
  requests in the DB, pick one to work on, and try to help solve it in whatever way I
  can. When it&rsquo;s solved, I&rsquo;ll mark it complete in the DB.</span></p>

