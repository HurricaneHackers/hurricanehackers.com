  "h.wpgmdor2g0it"></a><span>SandyIssuesMap</span></h1>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c2 c18">SeeClickFix is a web and mobile platform that allows
  citizens to report non-emergency issues to their neighbors, and to their local
  government. It is a crowd-sourced community issue platform. &nbsp;For the purposed of
  leveraging SeeClickFix content for a storm-related hack/project, here are the
  options:</span></p>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c2 c3">CONTENT</span></p>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c2 c3">1) Widgets</span></p>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c2">SeeClickFix has a self-serve widget generator at</span>
  <span class="c0"><a class="c8" href=
  "http://seeclickfix.com/widget">http://seeclickfix.com/widget</a></span><span class=
  "c2">, which allows anyone to take advantage of SeeClickFix content on their own
  website. &nbsp;</span></p>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c2">There are three types of widgets to choose
  from:</span></p>

  <ol class="c17" start="1">
    <li class="c5 c4"><span class="c2">Text widget (list only, optionally linking to a
    map widget landing page)</span></li>

    <li class="c5 c4"><span class="c2">Map widget (&ldquo;advanced&rdquo;, with issue
    list and report)</span></li>

    <li class="c5 c4"><span class="c2">Map widget (&ldquo;simple&rdquo;, option to leave
    off issue list [deprecated soon])</span></li>
  </ol>

  <p class="c4 c9"></p>

  <p class="c4"><span class="c2">To create a &ldquo;storm widget&rdquo;, you can use
  the</span> <span class="c2 c3">filter by keyword</span><span class="c2">&nbsp;to only
  include issues reported with the following words:</span> <span class=
  "c2 c11">hurricane,storm,sandy,flood,flooding,power,electricity,damage</span><span class="c2">.
  [Feel free to suggest others].</span></p>

